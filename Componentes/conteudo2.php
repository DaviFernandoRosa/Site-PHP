    <h2 class="titulo2">Como Funciona a internet via rádio?</h2> 

    <p class="paragrafo1">
    	
       A Internet via rádio funciona de forma relativamente simples. O sinal é transmitido pelas torres que são distribuídas em pontos altos e com bom acesso ao perímetro atendido pelo provedor.<br><br>
      • Para receber a internet, uma antena é instalada na casa do usuário e fica responsável por captar do sinal emitido por uma das torres. Essa antena precisa ser instalada em um local em que a ligação, via sinais de rádio, entre o seu receptor e a torre de transmissão seja estabelecida.<br><br>
   • Para fazer a conversão do sinal de rádio para internet, a antena é conectada a um modem. Este modem, por sua vez, é que transforma o sinal e o transmite para a placa de rede do computador ou smartphone.<br><br>
   • QUALIDADE:<br><br>
A Internet via rádio pode ser de boa qualidade se a instalação for bem feita e a configuração for realizada por profissionais adequados.
Outro adendo: Os provedores de internet via rádio possuem maior alcance e, geralmente, atendem áreas periféricas (onde a internet banda larga ainda não chega), como as áreas rurais, por exemplo.<br><br>
• SIMPLICIDADE DE INSTALAÇÃO E MANUTENÇÃO:<br><br>
Por usar um sinal de rádio e transmiti-lo através de antenas e torres, esse tipo de conexão também dispensa a instalação de uma linha telefônica, tornando o processo menos complicado e mais rápido.</p>


